package com.example.HW2.objects;

public class Coin {

    private int coinX;
    private int coinY;

    public Coin(){
        coinX = 0;
        coinY = 0;
    }

    public int getCoinX() {
        return coinX;
    }

    public void setCoinX(int coinX) {
        this.coinX = coinX;
    }

    public int getCoinY() {
        return coinY;
    }

    public void setCoinY(int coinY) {
        this.coinY = coinY;
    }


}


